// loop

// for loop
for (let i = 0; i < 10; i++) {
    console.log(`Saying boomboombammbaam for ${i + 1} times`);
}

// while loop
i = 0;
while (i < 10) {
    console.log(`Saying yeay yay yeeah for ${i + 1} times`);
    i++;
}

// do-while loop
i = 0;
do {
    console.log(`Saying hornyIsTheBest for ${i + 1} times`);
    i++;
} while (i < 10)

// break
i = 0
while (true) {
    if (i >= 10)
        break;
    console.log(`Saying javascript blaytt for ${i + 1} times`);
    i++;
}