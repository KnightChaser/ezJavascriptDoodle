let money = 12;
money += 11;
money += 1;

console.log(`Balance: ${money} BTC`);			// 24

money++;
console.log(`Balance: ${money} BTC`);			// 25

++money;
console.log(`Balance: ${money} BTC`);			// 26
console.log(`Balance: ${++money} BTC`);         // 27
